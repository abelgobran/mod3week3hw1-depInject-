package mod3wk3hw1;

public class House {
	int miles;
	String make;
	Sting model;
	public int getMiles() {
		return miles;
	}
	public void setMiles(int miles) {
		this.miles = miles;
	}
	public String getMake() {
		return make;
	}
	public void setMake(String make) {
		this.make = make;
	}
	public Sting getModel() {
		return model;
	}
	public void setModel(Sting model) {
		this.model = model;
	}

}
